@app.cell
def _(data, mass2):
    freqs_khz = [5, 10, 15, 20, 25]

    def _prep_pulse(ch: mass2.Channel) -> mass2.Channel:
        # Standard analysis steps: gather simple pulse features first, cut outliers.
        return (
            ch.summarize_pulses()
            .with_good_expr_pretrig_rms_and_postpeak_deriv()
        )

    def _sweep_filters_and_dc(ch: mass2.Channel) -> mass2.Channel:
        # Filter with multiple different f3db values
        for f in freqs_khz:
            f_3db = f * 1000.0
            y_col = f"5lagy_{f}khz"
            x_col = f"5lagx_{f}khz"
            y_dc_col = f"5lagy_dc_{f}khz"

            # Apply optimal filter
            ch = ch.filter5lag(
                pulse_col="pulse",
                peak_y_col=y_col,
                peak_x_col=x_col,
                f_3db=f_3db,
            )

            # Followed by drift correction (on the peak estimates computed by the filter)
            import polars as pl
            ch = ch.driftcorrect(
                indicator_col="pretrig_mean",
                uncorrected_col=y_col,
                corrected_col=y_dc_col,
                use_expr=pl.lit(True)
            )
        return ch

    data2 = data.map(_prep_pulse).map(_sweep_filters_and_dc)
    return (data2,)


