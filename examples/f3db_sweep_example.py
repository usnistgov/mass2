@app.cell
def _(data, mass2):
    freqs_khz = [5, 10, 15, 20, 25]

    def _analyze_and_sweep(ch: mass2.Channel) -> mass2.Channel:
        # Standard analysis steps
        ch = (
            ch.summarize_pulses()
            .with_good_expr_pretrig_rms_and_postpeak_deriv()
            .driftcorrect(uncorrected_col="peak_value")
        )

        # Filter with multiple different f3db values
        for f in freqs_khz:
            f_3db = f * 1000.0
            y_col = f"5lagy_{f}khz"
            x_col = f"5lagx_{f}khz"

            # Apply optimal filter
            ch = ch.filter5lag(
                pulse_col="pulse",
                peak_y_col=y_col,
                peak_x_col=x_col,
                f_3db=f_3db,
            )
        return ch

    data2 = data.map(_analyze_and_sweep)
    return (data2,)


