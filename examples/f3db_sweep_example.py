@app.cell
def _(data2, mass2, pl):
    import dataclasses

    def extract_filter_history(ch: mass2.Channel) -> pl.DataFrame:
        # Instead of doing external bookkeeping where we maintain separate dictionaries or arrays 
        # to track which filter parameter corresponds to which column, we can inspect `ch.steps`.
        # mass2 already saved all the data we need, lets just put it in a DataFrame.
        records = []

        # We step through the history of operations performed on this channel to find the filter pass.
        for step in ch.steps:
            if isinstance(step, mass2.core.filter_steps.OptimalFilterStep):
                # The OptimalFilterStep contains a reference to the filter object created for this channel.
                filter_obj = step.filter

                # We don't need to know exactly which parameters exist on a given filter class ahead of time.
                # By dynamically inspecting the dataclass fields, we automatically extract every scalar 
                # property (e.g. f_3db, fmax, cut_pre, cut_post, predicted_v_over_dv) without hardcoding them.
                filter_scalars = {}
                for field in dataclasses.fields(filter_obj):
                    val = getattr(filter_obj, field.name)
                    # Include standard scalar types; ignore large arrays like the filter 'values'
                    if isinstance(val, (int, float, str, bool)) or val is None:
                        filter_scalars[field.name] = val

                # Retrieve the resulting column names generated by this specific filter step
                peak_x_col = step.output[0]
                peak_y_col = step.output[1]

                # Determine the corresponding drift-corrected column names. 
                # (Since our loop applies drift correction to the y output, naming it 5lagy_dc_{f}khz)
                y_dc_col = peak_y_col.replace("5lagy_", "5lagy_dc_")

                # We bundle everything for this filter iteration into one dictionary representing a row.
                record = {
                    "ch_num": ch.ch_num,
                    "output_y_col": y_dc_col,
                    "output_x_col": peak_x_col,  # 5lagx doesn't get drift corrected!
                }
                record.update(filter_scalars)
                records.append(record)

        if not records:
            return pl.DataFrame()

        # Convert the records into a Polars DataFrame for efficient filtering and grouping later.
        return pl.DataFrame(records)

    def collect_all_filter_histories(channels: mass2.Channels) -> pl.DataFrame:
        df_list = []
        for ch_num, ch in channels.channels.items():
            df_list.append(extract_filter_history(ch))

        if len(df_list) == 0:
            return pl.DataFrame()

        df_filters = pl.concat(df_list)

        # Because we extracted every parameter iteratively, parameters that weren't explicitly provided
        # during filter creation (like `fmax` which defaults to None) will show up as completely null columns.
        # We can cleanly drop all of these unpopulated fields in one pass across the whole consolidated dataset.
        import polars.selectors as cs
        return df_filters.drop(cs.by_dtype(pl.Null))

    df_filters = collect_all_filter_histories(data2)
    return (df_filters,)


