@app.cell
def _(df_filters, mass2, pl, plt):
    plt.figure(figsize=(8, 5))

    # Get all unique channels and sort them
    channels = df_filters["ch_num"].unique().sort()

    for ch_num in channels:
        # Filter down to the single channel and sort by f_3db to ensure lines draw consecutively
        df_ch = df_filters.filter(pl.col("ch_num") == ch_num).sort("f_3db")

        # Plot f_3db against the automatically extracted predicted_v_over_dv field
        plt.plot(
            df_ch["f_3db"].to_numpy(), 
            df_ch["predicted_v_over_dv"].to_numpy(), 
            marker='o', 
            label=f"Ch {ch_num}"
        )

    plt.xlabel("f_3db (Hz)")
    plt.ylabel("predicted_v_over_dv (eV)")
    plt.title("Predicted Energy Resolution vs Filter Cutoff")

    # Only display legend if there are 15 or fewer channels so things don't get too messy
    if len(channels) <= 15:
        plt.legend(bbox_to_anchor=(1.05, 1), loc='upper left')

    plt.grid(True)
    plt.tight_layout()

    mass2.show()
    return


