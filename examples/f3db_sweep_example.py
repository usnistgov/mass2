@app.cell
def _(data2, mass2, pl):
    import dataclasses

    def extract_filter_history(ch: mass2.Channel) -> pl.DataFrame:
        records = []

        # Inspect all the steps that ran on the channel
        for step in ch.steps:
            if isinstance(step, mass2.core.filter_steps.OptimalFilterStep):
                # We found an OptimalFilterStep!
                filter_obj = step.filter

                # Extract choices and metrics from the filter dynamically
                filter_scalars = {}
                for field in dataclasses.fields(filter_obj):
                    val = getattr(filter_obj, field.name)
                    # Include standard scalar types, ignore arrays like 'values'
                    if isinstance(val, (int, float, str, bool)) or val is None:
                        filter_scalars[field.name] = val

                # Retrieve the column names generated by this step
                peak_x_col = step.output[0]
                peak_y_col = step.output[1]

                record = {
                    "ch_num": ch.ch_num,
                    "output_y_col": peak_y_col,
                    "output_x_col": peak_x_col,
                }
                record.update(filter_scalars)
                records.append(record)

        # If no optimal filter steps found, return empty df
        if not records:
            return pl.DataFrame()

        return pl.DataFrame(records)

    def collect_all_filter_histories(channels: mass2.Channels) -> pl.DataFrame:
        df_list = []
        for ch_num, ch in channels.channels.items():
            df_list.append(extract_filter_history(ch))

        if len(df_list) == 0:
            return pl.DataFrame()

        df_filters = pl.concat(df_list)
        import polars.selectors as cs
        # Drop columns that are entirely null
        return df_filters.drop(cs.by_dtype(pl.Null))

    df_filters = collect_all_filter_histories(data2)
    return (df_filters,)


